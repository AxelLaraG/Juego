def dibujaFinnJuego(x, y, z, camina, frente, izquierda, derecha):
    global centro, cajaCabeza
    glPushMatrix()
    glTranslate(x, y, z)
    centro = [x, y, z]
    cajaCabeza = mBall(0.55, 0.55, 0.55, centro)
    if frente:
        glRotate(180, 0.0, 1.0, 0.0)
    if izquierda:
        glRotate(-90, 0.0, 1.0, 0)
    if derecha:
        glRotate(90, 0.0, 1.0, 0)
    if camina:
        dibujaCabeza(" ")
        dibujacuerpo(" ")
        dibujapiernas("C")
        dibujabrazos("C")
    else:
        dibujaCabeza(" ")
        dibujacuerpo(" ")
        dibujapiernas(" ")
        dibujabrazos(" ")
    glPopMatrix()


